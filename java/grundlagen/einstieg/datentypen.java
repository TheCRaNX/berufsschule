package schule.grundlagen.einstieg;

public class datentypen {
    public static void main(String[] args) {
        int zahl = 42;              // Ganzzahl
        double pi = 3.14;           // Gleitkommazahl
        char buchstabe = 'A';       // Zeichen
        boolean istWahr = true;     // Wahrheitswert
        String text = "Hallo";      // Zeichenverkettung


        System.out.println(zahl);
        System.out.println(pi);
        System.out.println(buchstabe);
        System.out.println(istWahr);
        System.out.println(text);
    }
}